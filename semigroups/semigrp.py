def bar_dict(f, SCCs):
    #gives transformation as dictionary, with keys as input, values as image
    d = {}
    for SCC in SCCs:
        image = hit(f, SCC[0])
        for SCC2 in SCCs:
            if image in SCC2:
                d[SCC] = SCC2
                break
    return d


